## Crie um programa que faça o computador jogar Jokenpô com o usuário.

## O programa deve permitir que o usuário escolha uma das opções:

## Pedra
## Papel
## Tesoura

## Em seguida, o computador deve escolher uma opção aleatória.
## Depois, o programa deve comparar as escolhas e informar o resultado:

## Usuário venceu
## Computador venceu
## Empate

## O programa também deve exibir na tela a escolha do usuário e a escolha do computador.